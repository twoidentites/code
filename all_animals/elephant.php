        <h2>ELEPHANT'S</h2><hr>
        <p>A three-foot-deep watering hole in northeast India hums with buzzing insects and chirping birds. Suddenly seven Asian elephants stomp through the grass surrounding the pool and splash into the water. It’s bath time for the herd.
        Asian elephants live in the tropical forests and grasslands of Southeast Asia. They’re a little smaller than African elephants but can grow nine feet tall and weigh nearly six tons. That’s heavier than two medium-size cars! Even newborns are on the hefty side, weighing some 200 pounds. Given their size it’s no surprise that these guys are big on food. Adults can eat 300 pounds in one day, nearly 75 times what the average human eats! An Asian elephant’s ideal meal includes roots, grasses, fruit, and bark.

Much of an Asian elephant’s time is spent searching for grub. In fact the animal may travel 50 miles a day to find food. Asian elephants also enjoy taking dips in the water. To cool down their backsides, the animals will fill their trunks with water and spray it over themselves like a hose.
Asian elephants are gentle and super-social. Related females live with their young in herds. (Adult males go off on their own.) To communicate with each other they use rumbles, bellows, growls, and moans. Some of their vocalizations can be heard more than a mile away. Talk about a long-distance call!

When an elephant gets a whiff of something interesting, it sniffs the air with its trunk raised up like a submarine periscope. If threatened, an elephant will also use its trunk to make loud trumpeting noises as a warning.

Elephants are social creatures. They sometimes hug by wrapping their trunks together in displays of greeting and affection. Elephants also use their trunks to help lift or nudge an elephant calf over an obstacle, to rescue a fellow elephant stuck in mud, or to gently raise a newborn elephant to its feet. And just as a human baby sucks its thumb, an elephant calf often sucks its trunk for comfort. One elephant can eat 300 pounds (136 kilograms) of food in one day.

People hunt elephants mainly for their ivory tusks. Adult females and young travel in herds, while adult males generally travel alone or in groups of their own.

